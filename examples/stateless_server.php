<?php

/**
 * Minimal stateless MCP server (2026-07-28 revision).
 *
 * Under the 2026-07-28 spec revision there is no initialize handshake, no
 * Mcp-Session-Id header, and no server-held connection state. Every request
 * is self-contained: it carries the protocol version, client info, and client
 * capabilities in its `_meta` envelope, and clients learn what the server
 * offers from the cacheable `server/discover` method (the SDK stamps the
 * required `ttlMs`/`cacheScope` caching hints on discover and list results
 * automatically).
 *
 * None of that requires anything special in server code — register features,
 * call run(). The same script serves
 *   - modern clients (2026-07-28) statelessly, one self-contained request at
 *     a time, and
 *   - legacy clients (2024-11-05 … 2025-11-25) through the classic initialize
 *     handshake,
 * because the SDK detects the era of each incoming request.
 *
 * The stateless model is what makes MCP practical on typical PHP web hosting
 * (cPanel/Apache/FPM), where every HTTP request is served by a fresh process.
 *
 * Run:
 *   stdio: php examples/stateless_server.php
 *   HTTP:  php -S localhost:8000 examples/stateless_server.php
 *
 * Then connect with examples/client_negotiation.php, examples/client_http.php,
 * or MCP Inspector (npx @modelcontextprotocol/inspector).
 */

require 'vendor/autoload.php';

use Mcp\Server\McpServer;

$server = new McpServer('stateless-example-server');

$server
    // A plain tool. The input schema is generated by reflection from the
    // callback signature (two required numbers).
    ->tool('add-numbers', 'Adds two numbers together', function (float $a, float $b): string {
        return 'Sum: ' . ($a + $b);
    })

    // A tool with structured output: when an outputSchema is declared, the
    // callback's return value becomes the result's `structuredContent` (and
    // is also serialized into a text block for clients that ignore it).
    ->tool(
        'server-time',
        'Returns the current server time',
        fn (): array => ['iso' => date('c'), 'unix' => time()],
        outputSchema: [
            'type' => 'object',
            'properties' => [
                'iso' => ['type' => 'string', 'description' => 'ISO 8601 timestamp'],
                'unix' => ['type' => 'number', 'description' => 'Unix timestamp'],
            ],
            'required' => ['iso', 'unix'],
        ],
    )

    // A static resource.
    ->resource(
        uri: 'info://server',
        name: 'Server Info',
        callback: fn (): string => 'PHP ' . PHP_VERSION . ' — stateless MCP example server',
        description: 'Basic information about this server',
    )

    // A resource template (RFC 6570 level 1): greeting://{name} extracts the
    // variable and passes it to the callback by name.
    ->resourceTemplate(
        uriTemplate: 'greeting://{name}',
        name: 'Personal greeting',
        callback: fn (string $name): string => "Hello, {$name}!",
        description: 'A greeting for the person named in the URI',
    )

    // A prompt. Returning a string produces a single user message.
    ->prompt('summarize', 'Builds a summarization prompt', function (string $text): string {
        return "Please summarize the following text:\n\n{$text}";
    })

    // run() auto-selects stdio on the CLI and HTTP under a web server.
    ->run();
